// TODO remove, this demo shouldn't need to reset the theme.

const defaultTheme = createTheme();

export default function SignIn({ onSuccessfulLogin }) {

    const [email, setEmail] = useState('');
    const [password, setPassword] = useState('');
    //error message handling
    const [errorMessage, setErrorMessage] = useState('');
    //const [registrationToggle, setRegistrationToggle] = useState(false);

    //function from the context
    const { user } = useUser();
    
    function submitLogin(e) {
        e.preventDefault();
      
        client.post("/api/login", {
            email: email,
            password: password
        })
        .then(function(res) {
            if (typeof onSuccessfulLogin === "function") {
                onSuccessfulLogin(email);
                setErrorMessage(''); 
            } else {
                console.error('onSuccessfulLogin is not provided or is not a function');
            }
        })
        .catch(function(error) {
            //handle the error
            setErrorMessage('Email or password is incorrect'); 
        });
      }
      
    
      
      
      
      

  return (
    <ThemeProvider theme={defaultTheme}>
      <Container component="main" maxWidth="xs">
        <CssBaseline />
        <Box
          sx={{
            marginTop: 8,
            display: 'flex',
            flexDirection: 'column',
            alignItems: 'center',
          }}
        >
          <Avatar sx={{ m: 1, bgcolor: 'secondary.main' }}>
            <LockOutlinedIcon />
          </Avatar>
          <Typography component="h1" variant="h5">
            Sign in
          </Typography>
          <Box component="form" onSubmit={submitLogin} noValidate sx={{ mt: 1 }}>
            <TextField
              margin="normal"
              required
              fullWidth
              id="email"
              label="Email Address"
              name="email"
              autoComplete="email"
              autoFocus
              value={email}
            onChange={e => setEmail(e.target.value)}
            />
            <TextField
              margin="normal"
              required
              fullWidth
              name="password"
              label="Password"
              type="password"
              id="password"
              autoComplete="current-password"
              value={password}
            onChange={e => setPassword(e.target.value)}
            />
            <Button
              type="submit"
              fullWidth
              variant="contained"
              sx={{ mt: 3, mb: 2 }}
            >
              Sign In
            </Button>
            {errorMessage && <div className="error-message" style={ {'color': "red", 'margin-top': '10px',}}>{errorMessage}</div>}
            <Grid container>
              <Grid item>
                <Link href="#" variant="body2">
                  {"Don't have an account? Sign Up"}
                </Link>
              </Grid>
            </Grid>
          </Box>
        </Box>
        <Copyright sx={{ mt: 8, mb: 4 }} />
      </Container>
    </ThemeProvider>
  );
}